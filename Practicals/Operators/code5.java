class Sixth{
        public static void main(String[] args){

                int a=10;
                int b=5;

                System.out.println(a+=3);  //a=a+3
                System.out.println(b-=2);  //b=b-2
                System.out.println(a*=2);  //a=a*2
                System.out.println(b/=3);  //b=b/3
                System.out.println(a%=5);  //a=a%5
        }
}

